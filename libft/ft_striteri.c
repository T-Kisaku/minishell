#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	s_i;

	s_i = 0;
	while (s[s_i])
	{
		f(s_i, &s[s_i]);
		s_i++;
	}
}
