#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	s_index;

	s_index = ft_strlen(s);
	while (s_index >= 0)
	{
		if ((unsigned char)s[s_index] == (unsigned char)c)
			return ((char *)&s[s_index]);
		s_index--;
	}
	return (NULL);
}
