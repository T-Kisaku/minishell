#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*ptr;
	size_t				ptr_index;

	ptr = (const unsigned char *)s;
	ptr_index = 0;
	while (ptr_index < n)
	{
		if (ptr[ptr_index] == (unsigned char)c)
			return ((void *)&ptr[ptr_index]);
		ptr_index++;
	}
	return (NULL);
}
